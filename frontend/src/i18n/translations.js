export function translate(lang, key, vars) {
  const dict = translations[lang] || translations.fa;
  let str = dict[key] ?? translations.fa[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      str = str.replace(`{${k}}`, vars[k]);
    }
  }
  return str;
}
